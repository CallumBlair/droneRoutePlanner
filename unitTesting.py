class graphTesting(unittest.TestCase):
    def testGraphCreation(self):
        graph = a.graph()
        graph.addNode([1,2], "12")
        graph.addNode([1,3], "13")
        graph.addNode([1,4], "14")
        self.assertEqual(list(graph.getNodes()), ['12', '13', '14'], "graph creation failed")

    def testGraphSetStart(self):
        graph = a.graph()
        graph.addNode([1,1], "11")
        graph.addStart("11")
        self.assertEqual(graph.startNode, "11", "graph start node assignment failed")

    def testGraphEdge(self):
        graph = a.graph()
        graph.addNode([1,1], "11")
        graph.addNode([1,2], "12")
        graph.addEdge("11", "12", 1)
        edges = [graph.getNode("11").getNeighbors(), graph.getNode("12").getNeighbors()]
        self.assertEqual(edges, [[['12', 1]], [['11', 1]]], "graph edge addition failed")      
        
    def testGraphGridParse(self):
        graph = a.graph()
        graph.parseGrid(parseGridTest)
        self.assertEqual(list(graph.getNodes()),parseGraphExpectedResult, "parse grid failed") 



